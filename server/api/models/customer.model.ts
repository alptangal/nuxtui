import mongoose from 'mongoose'

const schema:mongoose.Schema=new mongoose.Schema({
    first_name:{
        type:String
    },
    middle_name:{
      type:String
  },
  last_name:{
    type:String
},
account:{
  username:{
    type:String
  },
  password:{
    type:String
  },
  status_active:{
    type:String
  },
  type:{
    type:String
  }
},
    avatar:{
        type:Object
    },
    banner:{
      type:Object
    },
    contacts:{
      type:Array
    },
    info:{
      type:Array
    },
    note:{
        type:String,
    },
    tags:{
        type:String,
    },
    history:{
      type:Array
    },
    created_by:{
        type:String
    },
    created_at:{
        type:Date
    },
    edited_by:{
        type:String
    },
    edited_at:{
        type:Date
    }
})
export default mongoose.model('Customer',schema)